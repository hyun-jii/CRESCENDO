package com.crescendo.spring.user.model.vo.ffinal;

import java.util.Date;

public class FinalVO
{
	private int finalNum;
	private int appNum;
	private String finalSta;
	private Date finalDate;
	
	
	public int getFinalNum()
	{
		return finalNum;
	}
	public void setFinalNum(int finalNum)
	{
		this.finalNum = finalNum;
	}
	public int getAppNum()
	{
		return appNum;
	}
	public void setAppNum(int appNum)
	{
		this.appNum = appNum;
	}
	public String getFinalSta()
	{
		return finalSta;
	}
	public void setFinalSta(String finalSta)
	{
		this.finalSta = finalSta;
	}
	public Date getFinalDate()
	{
		return finalDate;
	}
	public void setFinalDate(Date finalDate)
	{
		this.finalDate = finalDate;
	}
	
	
}
